DELETE FROM 
    StartTimeChange 
;

WITH 
    ranked AS ( 
        SELECT 
            *, 
            ROW_NUMBER() OVER ( 
                PARTITION BY 
                    eventYear,
                    eventMonthDay,
                    raceCourseCode,
                    eventRound,
                    eventDay,
                    raceNumber
                ORDER BY 
                    creationDate DESC 
            ) AS rn 
        FROM 
            StartTimeChange_NoPK 
    ) 
INSERT INTO 
    StartTimeChange 
SELECT 
    dataCategory,
    creationDate,
    eventYear,
    eventMonthDay,
    raceCourseCode,
    eventRound,
    eventDay,
    raceNumber,
    announcementDateTime,
    newStartTime,
    oldStartTime
FROM 
    ranked 
WHERE 
    rn = 1 
;

COMMIT;

